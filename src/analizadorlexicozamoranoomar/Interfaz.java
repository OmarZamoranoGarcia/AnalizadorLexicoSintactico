    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
    
    if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
        try {
            // 1. Leer y mostrar archivo
            String codigo = new String(Files.readAllBytes(fileChooser.getSelectedFile().toPath()));
            jTextArea1.setText(codigo);
            
            // 2. Configurar captura de terminal
            ByteArrayOutputStream terminalOutput = new ByteArrayOutputStream();
            PrintStream originalOut = System.out;
            System.setOut(new PrintStream(terminalOutput));
            
            // 3. Ejecutar an치lisis
            Sintax parser = new Sintax(new LexerCUP(new StringReader(codigo)));
            parser.parse();
            
            // 4. Restaurar System.out
            System.setOut(originalOut);
            
            // 5. Verificar si hubo mensajes
            boolean terminalVacia = terminalOutput.toString().trim().isEmpty();
            
            // 6. Mostrar resultado
            if (terminalVacia) {
                jTextArea1.append("\n\nCompilado correctamente");
            } else {             
                jTextArea1.append(terminalOutput.toString());
            }
            
        } catch (Exception ex) {
            jTextArea1.append("\n\nError durante el an치lisis: " + ex.getMessage());
        }
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    public static int insertarId(String id) {
        if (!tabla_de_simbolos.containsKey(id)) {
            tabla_de_simbolos.put(id, tabla_de_simbolos.size()); 
        }
        return tabla_de_simbolos.get(id);
    }
